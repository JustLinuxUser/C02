char	*ft_strcpy(char *dest, char *src)
{
	int	iter;

	iter = 0;
	while (src[iter] != '\0')
	{
		dest[iter] = src[iter];
		iter++;
	}
	dest[iter] = '\0';
	return (dest);
}
